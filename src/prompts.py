# Phase 1 -- Factual Site Intelligence Only
# NOTE: Phase 1 does NOT describe boundary geometry -- the image gen model
# reads the visual topo directly for shape. Text boundary descriptions from
# a VLM are unreliable and COMPETE with the visual signal, causing the model
# to draw wrong shapes. Phase 1 only extracts factual locational data:
#   - Where the road entry point is
#   - Which low-point basins are confirmed INSIDE the boundary
#   - Site-specific planning constraints (not shape descriptions)
